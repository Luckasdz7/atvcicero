package atvcicero;

public class Cauluadora1 {
	private float a,b;
	

	public float getA() {
		return a;
	}


	public void setA(float a) {
		this.a = a;
	}


	public float getB() {
		return b;
	}


	public void setB(float b) {
		this.b = b;
	}


	public float soma() {
		return a + b;
	}
	public float subtrair() {
		return a - b;
	}
	public float dividir() {
		return a / b;
	}
	 public float multilicar() {
		return a * b;
	}

}
